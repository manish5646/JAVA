class Swap
{
public static void main(String[]args)
{
int a=10;
int b=20;
System.out.println("Befor Swapping..");
System.out.println("a="+a);
System.out.println("b="+b);

int  c=0;

c=b;
b=a;
a=c;

System.out.println("After Swapping..");
System.out.println("a="+a);
System.out.println("b="+b);
 }
}